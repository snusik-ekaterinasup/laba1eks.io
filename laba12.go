package main

import "fmt"

func main() {
 // Создание переменных различных типов
 var myInt int = 20
 var myFloat float64 = 14.88
 var myString string = "ok bro"
 var myBool bool = false 

 // Вывод значений переменных на экран
 fmt.Println("Integer value:", myInt)
 fmt.Println("Float value:", myFloat)
 fmt.Println("String value:", myString)
 fmt.Println("Boolean value:", myBool)
}